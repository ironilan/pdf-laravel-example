    <h4 class="mb0 uppercase">9. Monitoreo y Evaluación del Programa</h4>
    <p>La consolidación de el/la (nombre del programa) se realizará a través de actividades de monitoreo y evaluación a fin de asegurar su ejecución en términos de eficiencia, eficacia e impacto así como el logro de los objetivos de formación e identificar variaciones para considerar las adecuaciones y ajustes oportunamente.</p>
    <p class="mb0">Entre los indicadores de monitoreo y evaluación que se consideran tenemos:</p>
    <div class="subtitulo">
        <ul>
            <li>Cumplimiento y calidad de los productos académicos.</li>
            <li>Satisfacción de los alumnos con el servicio educativo y sus aspectos contextuales y de soporte.</li>
            <li>Nivel de participación y compromiso de los estudiantes.</li>
            <li>Satisfacción del personal docentes.</li>
            <li>Funcionalidad de los sistemas de soporte y de gestión.</li>
        </ul>
    </div>
    <p>Algunos de los instrumentos de monitoreo y evaluación a implementar son de naturaleza cuantitativa y cualitativa y tienen como propósito conocer cuánto se está avanzando y cómo se dan esos procesos y cambios en los estudiantes y en la organización misma, los que se adecuarán en el tiempo según la pertinencia de los mismos. </p>
