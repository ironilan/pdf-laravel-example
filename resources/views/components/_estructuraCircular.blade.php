    	<h5 class="mb0" style="text-decoration: underline;">Enfoque por competencias</h5>
    	<p>El enfoque por competencias en la universidad busca la pertinencia, la equidad y la calidad en la formación profesional y humana, enfocada principalmente en el desarrollo integral del estudiante como una respuesta a los requerimientos de la sociedad contemporánea. </p>
    	<p>«Se asume que la competencia es un saber actuar complejo que se apoya sobre la movilización y la utilización eficaz de una variedad de recursos internos y externos»  «al resolver situaciones o problemas del contexto académico, profesional, personal, político o social; actuando con idoneidad, con compromiso ético y buscando la mejora continua».</p>
    	<p>Por ello, un egresado será competente en la medida en que sepa actuar de manera pertinente, en un contexto determinado, y sepa escoger y movilizar, tanto recursos personales, como externos. Para ello es necesario contar con una visión del estudiante que se quiere formar, las competencias específicas y genéricas que desarrollará, los contenidos formativos generales y de especialidad distribuidos en el plan de estudio y las estrategias didácticas y de evaluación que permitan concretizar los elementos anteriores.</p>


