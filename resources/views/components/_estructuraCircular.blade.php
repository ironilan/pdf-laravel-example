    	<p>La universidad coloca en el centro del debate, la responsabilidad del conjunto de sus miembros en fortalecer los rasgos que le dan identidad en sus actividades de formación, investigación y servicios. La producción científica, su rigor metodológico y ético, es garantía de su responsabilidad social. La universidad, como toda organización, produce impactos en el ambiente a través de los procesos cotidianos de producción de servicios y bienes. Sus miembros tienen el enorme desafío de mejorar su ecoeficiencia y asumir una perspectiva ecológica y de protección del medio ambiente.</p>
