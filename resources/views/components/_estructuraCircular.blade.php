    	<h5 class="mb0">7.1 Fundamentación </h5>
    	<h5 class="mb0" style="text-decoration: underline;">Modelo Educativo</h5>
    	<p>Respetuosa de la pluralidad de pensamiento y de la diversidad de modelos epistemológicos, la Universidad Peruana Cayetano Heredia promueve la creatividad e innovación, incorporándola a su misión institucional, con el propósito de: «Brindar formación profesional integral centrada en la persona; generar y difundir conocimiento, ciencia y tecnología y ofrecer servicios especializados con excelencia y responsabilidad social»</p>
    	<p>La excelencia académica, entendida como rigurosidad académica y búsqueda de mejora continua, con espíritu crítico para aproximarnos a la verdad, pone el énfasis en la investigación como el principal medio de creación del conocimiento que ordena, sistematiza y comunica sus hallazgos.</p>
    	<p>El humanismo, entendido tanto por su énfasis en el desarrollo armónico e integral de los profesionales, en su dimensión física, social, emocional, intelectual y espiritual, como por el interés en la realización plena de las personas, se enlaza con el compromiso social de la institución. En función de este, reconoce, valora y respeta a las diferentes culturas, preserva el medio ambiente, así como impulsa el desarrollo científico, social y tecnológico del país, para la atención de calidad y con equidad a las personas y a la problemática de las comunidades con las que interactuamos</p>
    	<p>Con la finalidad de cumplir con la filosofía institucional y afrontar los retos educativos en un mundo tan complejo y cambiante se consideran los siguientes ejes que orientan el diseño e implementación de las diversas propuestas formativas que ofrece la institución.</p>
    	<p>Estos ejes son contenidos fundamentales que deben estar incluidos en todos los programas, y tienen por finalidad promover la adquisición de valores y actitudes positivas para actuar con compromiso ético frente a la solución de determinadas situaciones o problemas del contexto.</p>
    	<p>Es así, como la producción del conocimiento, la responsabilidad social, la investigación, innovación y emprendimiento, el enfoque por competencias con ética y profesionalismo son los ejes transversales de todos los programas profesionales que desarrolla la Universidad y por ende el programa profesional de la / del (nombre del programa)</p>

