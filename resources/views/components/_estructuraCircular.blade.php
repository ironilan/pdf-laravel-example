        <h5 class="mb0">Estrategias de Formación </h5>
        <p>El modelo educativo de la Universidad Peruana Cayetano Heredia considera la actividad del docente centrado en el aprendizaje del estudiante, quien ocupa el lugar central en este proceso. “Esto supone, para el estudiante, un papel más activo, un mayor compromiso y responsabilidad por su propio aprendizaje y un enfoque profundo en su estilo de aprendizaje”. Para el docente, por ende, significa iniciar este proceso planificando las actividades a partir de la claridad que tiene respecto a los resultados de aprendizaje que espera logren sus estudiantes, definiendo lo que va a hacer y lo que harán los estudiantes para conseguir los resultados previstos y, finalmente, cómo se evaluará.</p>
        <p>El proceso de enseñanza y aprendizaje es un sistema de comunicación e interacción que tiene un propósito y a partir de él se seleccionan y aplican las estrategias pertinentes para el logro de los aprendizajes. En este proceso interactúan tres actores principales como los saberes como objeto de conocimiento, el docente como mediador y el estudiante como gestor de su propio proceso de aprendizaje.</p>
        <p>Este proceso se desarrolla considerando 06 principios de y para el aprendizaje enunciado en el modelo educativo que dirigen la labor de los docentes, hacia la consolidación de un aprendizaje significativo, profundo, autónomo, centrado en la resolución de problemas, que promueve la interacción social y el aprendizaje a través de redes.</p>
        <p>La Universidad Peruana Cayetano Heredia declara que la educación en la modalidad no presencial cambia esquemas tradicionales en el proceso de enseñanza- aprendizaje, tanto para el docente como para el estudiante, es promotora de una propuesta didáctica en la cual el estudiante autorregula su aprendizaje, como un proceso de autodirección, transforma sus aptitudes mentales en competencias académicas, es el docente quien encamina este aprendizaje.</p>
